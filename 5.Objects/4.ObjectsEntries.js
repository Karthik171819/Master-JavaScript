//In JavaScript, entries() is a method used to get key-value pairs from arrays or objects.
const obj = {
    name: "lok",
    age: 40,
    loc: "NYC"
}
console.log(Object.entries(obj));

//another use of entries it will arrange the integer key from asec to desc
const num = {
    12: "hi",
    8: "hy",
    1:"kk",
    100: "jnjnd"
}
console.log(Object.entries(num));
