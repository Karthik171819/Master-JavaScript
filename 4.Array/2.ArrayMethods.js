let player = ["Abd", "Virat", "Dhoni", "Sachin"];
player.push("Surya"); //used to push the value at the end of an array.

//to insert a value at initial position 
player.unshift("surya")

//to remove the end of an element in array
player.pop(); //surya poped

//to rem ove the first value of an array
player.shift(); //surya removed

//to find out the element index position in array by giving the certain exact value
player.indexOf('Abd');
